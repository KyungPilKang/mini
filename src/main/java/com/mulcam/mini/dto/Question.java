package com.mulcam.mini.dto;

public class Question {
	private String no;
	private String type;
	private String id;
	private String name;
	private String email;
	private String phone;
	private String title;
	private String qtext;
	private String atext;
	private String answerConfirm;
	private String qmodal;
	private String amodal;
	public Question() {}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getQtext() {
		return qtext;
	}
	public void setQtext(String qtext) {
		this.qtext = qtext;
	}
	public String getAtext() {
		return atext;
	}
	public void setAtext(String atext) {
		this.atext = atext;
	}
	public String getAnswerConfirm() {
		return answerConfirm;
	}
	public void setAnswerConfirm(String answerConfirm) {
		this.answerConfirm = answerConfirm;
	}
	public String getQmodal() {
		return qmodal;
	}
	public void setQmodal(String qmodal) {
		this.qmodal = qmodal;
	}
	public String getAmodal() {
		return amodal;
	}
	public void setAmodal(String amodal) {
		this.amodal = amodal;
	}
}
